#requires -Version 7
<#
.SYNOPSIS
  Re-sync the vendored core libraries from the upstream FFXIV-Spanish repo.

.DESCRIPTION
  vendor/ was SEEDED from upstream XivSpanish.Core / XivSpanish.GameData, but it is now owned,
  hand-editable code in this repo (the old read-only rule was lifted 2026-06-24). This script
  re-imports upstream by WIPING and recopying vendor/, so it OVERWRITES any local edits. It is
  therefore guarded behind -Force: run it only when you intend to adopt upstream's state (losing
  local changes) or after porting your edits upstream. After running, review the diff, rebuild,
  and commit. Provenance (upstream commit + sync date) is written to vendor/VENDORED.md.

.PARAMETER Upstream
  Path to the upstream FFXIV-Spanish repo. Defaults to the sibling directory of this repo.
.PARAMETER Force
  Required acknowledgement that this overwrites local vendor/ edits with upstream.
#>
[CmdletBinding()]
param(
    [string]$Upstream = "$PSScriptRoot/../../FFXIV-Spanish",
    [switch]$Force
)
$ErrorActionPreference = 'Stop'

if (-not $Force) {
    throw "sync-vendor.ps1 SOBREESCRIBE vendor/ con upstream (pierde ediciones locales). " +
          "Re-ejecuta con -Force si es lo que quieres."
}

$repoRoot   = (Resolve-Path "$PSScriptRoot/..").Path
$vendorRoot = Join-Path $repoRoot 'vendor'
$projects   = @('XivSpanish.Core', 'XivSpanish.GameData')

if (-not (Test-Path $Upstream)) { throw "Upstream repo not found: $Upstream" }
$Upstream = (Resolve-Path $Upstream).Path

New-Item -ItemType Directory -Path $vendorRoot -Force | Out-Null

foreach ($proj in $projects) {
    $src = Join-Path $Upstream "src/$proj"
    $dst = Join-Path $vendorRoot $proj
    if (-not (Test-Path $src)) { throw "Missing upstream project: $src" }

    if (Test-Path $dst) { Remove-Item $dst -Recurse -Force }
    New-Item -ItemType Directory -Path $dst -Force | Out-Null

    # Copy source files only; skip build output (bin/obj).
    Get-ChildItem $src -Recurse -File |
        Where-Object { $_.FullName -notmatch '[\\/](bin|obj)[\\/]' } |
        ForEach-Object {
            $rel    = $_.FullName.Substring($src.Length).TrimStart('\', '/')
            $target = Join-Path $dst $rel
            New-Item -ItemType Directory -Path (Split-Path $target) -Force | Out-Null
            Copy-Item $_.FullName $target -Force
        }
    Write-Host "Vendored $proj"
}

# XivSpanish.Packaging: vendor ONLY the reusable, library-grade primitives from the upstream
# Packager TOOL. Its Program.cs (a top-level Main + packaging glue) is intentionally NOT vendored;
# that orchestration is re-implemented in src/FFXIVSpanishPatcher.Pipeline, which is exactly the
# kind of glue an app shell owns. The .csproj is GENERATED here (the upstream tool has no library
# csproj) so that a re-sync, which wipes the directory, regenerates it.
$pkgSrc   = Join-Path $Upstream 'tools/XivSpanish.Packager'
$pkgDst   = Join-Path $vendorRoot 'XivSpanish.Packaging'
$pkgFiles = @('BaseExdSource.cs', 'ContaminationGuard.cs', 'ManifestSeStringGate.cs')

if (Test-Path $pkgDst) { Remove-Item $pkgDst -Recurse -Force }
New-Item -ItemType Directory -Path $pkgDst -Force | Out-Null
foreach ($file in $pkgFiles) {
    $sp = Join-Path $pkgSrc $file
    if (-not (Test-Path $sp)) { throw "Missing upstream packager primitive: $sp" }
    Copy-Item $sp (Join-Path $pkgDst $file) -Force
}

$pkgCsproj = @'
<!-- GENERATED by build/sync-vendor.ps1 - do not edit by hand. Wraps the vendored, library-grade
     primitives copied from the upstream Packager tool (its Program.cs Main is NOT vendored). -->
<Project Sdk="Microsoft.NET.Sdk">
  <PropertyGroup>
    <TargetFramework>net10.0</TargetFramework>
    <RootNamespace>XivSpanish.Packager</RootNamespace>
    <AssemblyName>XivSpanish.Packaging</AssemblyName>
  </PropertyGroup>
  <ItemGroup>
    <ProjectReference Include="..\XivSpanish.Core\XivSpanish.Core.csproj" />
    <ProjectReference Include="..\XivSpanish.GameData\XivSpanish.GameData.csproj" />
  </ItemGroup>
</Project>
'@
Set-Content -Path (Join-Path $pkgDst 'XivSpanish.Packaging.csproj') -Value $pkgCsproj -Encoding utf8
Write-Host "Vendored XivSpanish.Packaging (primitives only; csproj generated)"

# Record provenance.
$commit = (git -C $Upstream rev-parse HEAD).Trim()
$short  = (git -C $Upstream rev-parse --short HEAD).Trim()
$date   = Get-Date -Format 'yyyy-MM-dd HH:mm:ss zzz'
$md = @"
# Vendored code

vendor/ was SEEDED by copying core libraries from the upstream FFXIV-Spanish repo. It is now owned,
hand-editable code in this repo (read-only rule lifted 2026-06-24); it may diverge from upstream.
build/sync-vendor.ps1 -Force re-imports upstream by OVERWRITING vendor/, discarding local edits.

| Field    | Value |
| -------- | ----- |
| Upstream | FFXIV-Spanish |
| Commit   | $commit |
| Synced   | $date |
| Projects | $($projects -join ', '), XivSpanish.Packaging (primitives only) |
"@
Set-Content -Path (Join-Path $vendorRoot 'VENDORED.md') -Value $md -Encoding utf8

Write-Host "Wrote VENDORED.md (upstream $short, synced $date)"
